"""
OPERADORES DE COMPARAÇÃO
Operador da Igualdade ==
Operador da Diferença !=
Operador maior que >
Operador menor que <
Operador maior que ou igual a >=
Operador menor que ou igual a <=
"""

#Operador de atribuição =

x = 3    #tipo int
y = 5    #tipo int
z = 3.0  #tipo float
w = 8    #tipo int

print ("x == y?", x == y) #False 3 não é igual a 5
print ("x == z?", x == z) #True 3 = 3.0 apesar de serem TIPOS diferentes
print ("x != z?", x != z) #False
print ("x != y?", x != y) #True
print ("x < y?", x < y) #True
print ("x > y?", x > y) #False
print ("x <= y?", x <= y) #True
print ("x >= y?", x >= y) #False
print ("w >= y?", w >= y) #True
print ("w <= y?", w <= y) #False
